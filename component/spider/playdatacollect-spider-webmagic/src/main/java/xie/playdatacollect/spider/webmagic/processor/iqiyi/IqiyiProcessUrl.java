package xie.playdatacollect.spider.webmagic.processor.iqiyi;

public class IqiyiProcessUrl {
	private String href;
	private String title;
	private String firstProgramID;
	private String firstProgramUrl;
	private String programID;
	private String programUrl;

	public String getHref() {
		return href;
	}

	public void setHref(String href) {
		this.href = href;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getFirstProgramID() {
		return firstProgramID;
	}

	public void setFirstProgramID(String firstProgramID) {
		this.firstProgramID = firstProgramID;
	}

	public String getFirstProgramUrl() {
		return firstProgramUrl;
	}

	public void setFirstProgramUrl(String firstProgramUrl) {
		this.firstProgramUrl = firstProgramUrl;
	}

	public String getProgramID() {
		return programID;
	}

	public void setProgramID(String programID) {
		this.programID = programID;
	}

	public String getProgramUrl() {
		return programUrl;
	}

	public void setProgramUrl(String programUrl) {
		this.programUrl = programUrl;
	}
}
